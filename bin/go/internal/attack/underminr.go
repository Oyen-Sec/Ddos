// trySNISpoof attempts CDN bypass with a specific SNI + Host header combination
func trySNISpoof(edgeIP, targetHost, sniDomain string, parsed *url.URL) bool {
	dial := createDialer("", 5*time.Second)
	conn, err := dial("tcp", edgeIP+":443")
	if err != nil {
		return false
	}
	defer conn.Close()

	// TLS with spoofed SNI
	tlsConfig := &tls.Config{
		InsecureSkipVerify: true,
		ServerName:         sniDomain, // Spoofed SNI!
	}
	tlsConn := tls.Client(conn, tlsConfig)
	tlsConn.SetDeadline(time.Now().Add(5 * time.Second))

	if err := tlsConn.Handshake(); err != nil {
		return false
	}
	defer tlsConn.Close()

	// Send HTTP request with CORRECT Host header
	path := parsed.Path
	if path == "" {
		path = "/"
	}
	hostHeader := targetHost
	if parsed.Port() != "" {
		hostHeader = targetHost + ":" + parsed.Port()
	}

	req := fmt.Sprintf("GET %s HTTP/1.1\r\nHost: %s\r\nUser-Agent: Mozilla/5.0\r\nAccept: text/html,*/*\r\nConnection: close\r\n\r\n",
		path, hostHeader)

	_, err = tlsConn.Write([]byte(req))
	if err != nil {
		return false
	}

	// Read response - minimal read to check bypass
	respBuf := make([]byte, 4096)
	n, err := tlsConn.Read(respBuf)
	if err != nil && err != io.EOF {
		// Read may return data before EOF
	}
	_ = n

	respStr := string(respBuf[:n])

	// Check for bypass indicators
	// If we get a real response (not CDN block page), bypass works
	if !isCDNBlockPage(respStr) && !isCDNError(respStr) && len(respStr) > 100 {
		return true
	}
	return false
}

// isCDNBlockPage checks if response is a CDN block/challenge page
func isCDNBlockPage(resp string) bool {
	blockers := []string{
		"cloudflare-nginx", "cf-ray", "__cf_chl_opt", "cf_chl_proto",
		"Attention Required", "Just a moment", "Checking your browser",
		"Cloudflare", "Access Denied", "This website is using a security service",
		"Amazon CloudFront", "generated by cloudfront",
		"Fastly error", "AkamaiGHost",
	}
	lower := strings.ToLower(resp)
	for _, b := range blockers {
		if strings.Contains(lower, strings.ToLower(b)) {
			return true
		}
	}
	return false
}

// isCDNError checks for connection errors
func isCDNError(resp string) bool {
	if len(resp) == 0 {
		return true
	}
	errors := []string{"400", "403", "502", "503", "error", "timeout", "refused"}
	lower := strings.ToLower(resp)
	for _, e := range errors {
		if strings.Contains(lower, e) {
			return true
		}
	}
	return false
}

